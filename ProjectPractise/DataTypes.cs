using System;
using System.Collections.Generic;
using System.Text;

namespace ProjectPractise
{
    internal class DataTypes
    {

        internal void PDataTypes()
        {
            int a = 2;
            bool b = true;
            char c = 'A';
            double d = 3.14;
            float f = 3.14f;
            string s = "Hello, World";
            Console.WriteLine("Data Types a={0}, b={1}, c={2}, d={3}, f={4}, s={5}", a, b, c, d, f, s);
        }

        internal void DDataTypes()
        {
            Int16 a = 2;
            Boolean B = true;
            Char c = 'A';
            Double d = 3.14;
            Single f = 3.14f;
            String s = "Hello, World";
            Console.WriteLine("Data Types a={0}, b={1}, c={2}, d={3}, f={4}, s={5}", a, B, c, d, f, s);
        }
    }
}
